package aCoreJava;

public class P01_callig_data {
  /************************************************************
	When we write static we can call easily on other class
	Parameter = int & String
	Argument = (variable)==> a, b, c, d
  *************************************************************/
	public static int ram(int a, int b, int c, int d) {
	
		int sum = a + b + c + d;
		return sum;
		
	}
	
	public static void Shyam(String a, String b, String c, String d) {
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}
	
	
	
}
