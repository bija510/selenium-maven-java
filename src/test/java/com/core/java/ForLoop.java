package com.core.java;

public class ForLoop {

	public static void main(String[] args) {
		
		
		for(int i=0; i<=6; i=i+1) {  // i++ ==> i= i+1			
			System.out.println(i + "apple");				
		}
		
		System.out.println("=======================");
		
        for(int i=5; i>0; i--) {  			
			System.out.println(i);				
		}
        
        System.out.println("=======================");
        
        for(int i=0; i<18; i=i+3) { 			
			System.out.println(i);
		   		
		}	
		

	}

}
